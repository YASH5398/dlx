const I18nContext = createContext(undefined);
export function I18nProvider({ children }) {
    const [language, setLanguageState] = useState(() => localStorage.getItem('preferences.language') || 'English');
    useEffect(() => {
        localStorage.setItem('preferences.language', language);
    }, [language]);
    const t = useMemo(() => (key) => translations[language]?.[key] ?? key, [language]);
    const setLanguage = (l) => setLanguageState(l);
    const value = useMemo(() => ({ language, setLanguage, t }), [language]);
    return _jsx(I18nContext.Provider, { value: value, children: children });
}
export function useI18n() {
    const ctx = useContext(I18nContext);
    if (!ctx)
        throw new Error('useI18n must be used within I18nProvider');
    return ctx;
}
